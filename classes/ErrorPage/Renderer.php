	public static function button(
		String $text,
		String $color = Renderer::BUTTON_PRIMARY,
		String|\URL $link = "#",
		?String $icon = null,
		Array $class = [],
		bool $newTab = false,
		int $tris = 3
	): String {
		if ($link instanceof \URL)
			$link = $link -> out(false);

		$attrs = Array(
			"class" => array_merge([ "error-btn", $color ], $class),
			"href" => $link
		);

		if ($newTab) {
			$attrs["target"] = "_blank";
			$attrs["rel"] = "noopener";
		}

		$content = "";
		if (!empty($icon))
			$content .= static::icon($icon);

		if (!empty($text))
			$content .= HTMLBuilder::span([ "class" => "content" ], $text);

		for ($i = 0; $i < $tris; $i++)
			$content .= HTMLBuilder::div([ "class" => "tris tris-$i" ]);

		return HTMLBuilder::build("a", $attrs, $content);
	}

